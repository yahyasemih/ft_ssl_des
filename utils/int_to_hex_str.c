#include "int_to_hex_str.h"

void	int_to_hex_str(uint64_t data, char *dst)
{
	int		i;
	uint8_t	c;

	i = 0;
	while (i < 16)
	{
		c = data & 0xff;
		if (c / 16 < 10)
			dst[i] = c / 16 + '0';
		else
			dst[i] = (c / 16 - 10) + 'A';
		if (c % 16 < 10)
			dst[i + 1] = (c % 16) + '0';
		else
			dst[i + 1] = ((c % 16) - 10) + 'A';
		data >>= 8;
		i += 2;
	}
}
